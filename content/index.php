            <h4>Violín</h4>
            <p>Nacido en Madrid en los años de <em>la movida</em>, este violinista de formación clásica siempre tuvo inquietud e interés por otras músicas que no fuesen las estrictamente academicistas del conservatorio. Pero tocando el violín, no lo tuvo fácil para salirse del redil conservador. Pasaron años de búsqueda, palos de ciego y grupos varios y de variopintos estilos, desde la <em>chanson francesa</em> hasta el <em>reggae</em>, pasando por la música contemporánea o el <em>hillbilly</em>. Finalmente, en 2011 llegó a parar a Barcelona para continuar su formación en jazz y música moderna, en donde más tarde se reuniría con el resto de músicos para formar <b>Swingcaló</b>.</p>
