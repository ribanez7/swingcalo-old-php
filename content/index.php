            <p>Hicieron falta varios encuentros, ensayos y conciertos antes de dar con el nombre adecuado. <b>Swingcaló</b>. El swing de los gitanos. Lo que viene a ser el jazz manouche o gypsy jazz. Género que surgió en Francia a finales de los años 1930's al coincidir el jazz, recién llegado a Europa, con la música tradicional de los gitanos.<br />Ésta es la referencia en la música de <b>Swingcaló</b>, y de hecho no faltan en su repertorio temas clásicos del estilo, como <em>Minor Swing</em> o <em>Nuages</em>, compuestas y popularizadas por Django Reinhardt. Sin embargo, lo que caracteriza a <b>Swingcaló</b> es su capacidad para, de manera natural, adaptar temas de diversos estilos a su sonido particular. Siendo un grupo de manouche, su repertorio está arreglado expresamente para la formación. Sus temas van desde viejas melodías klezmer hasta la milonga argentina, pasando por ritmos flamencos o cubanos, pero en todo momento conserva su sonido reconocible y genuino, lo que hace que en sus conciertos haya un equilibrio de principio a fin.</p>
