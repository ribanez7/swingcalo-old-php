            <p id="biogra">Fruto del encuentro casual entre Laura y Rubén a raíz de un anuncio de clases de clarinete, surge la idea de formar un grupo de jazz manouche. Estos dos músicos de Terrassa no tardaron en encontrar a otros tres miembros más que se sumaron al proyecto. En poco tiempo ya estaba formado el quinteto, como el mítico <em>Quintette du Hot Club de France</em> de Django Reinhardt y Stéphane Grappelli pero con distinta formación. En este caso, una formación muy poco frecuente, ya que, si bien los instrumentos son habituales en el género, no es muy común encontrar una banda que cuente con clarinete, violín, dos guitarras y contrabajo.</p>
